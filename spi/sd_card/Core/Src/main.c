typedef struct {
	int data1;
	int data2;
	int data3;
}MY_DATA;


static void SDCard(void *pvParameters){

	MY_DATA dataWrite, dataRead;
	FIL fil;
	FATFS fs;
	UINT byteCounter;
	char text[30];

	dataWrite.data1 = 1;
	dataWrite.data2 = 2;
	dataWrite.data3 = 3;

	dataRead.data1 = 0;
	dataRead.data2 = 0;
	dataRead.data3 = 0;

	f_mount(&fs, "", 0);

	while(1){

		// Writes a binary file
		if(f_open(&fil,"datafile.dat", FA_OPEN_ALWAYS | FA_WRITE)==FR_OK){

			f_write(&fil, &dataWrite, sizeof(MY_DATA), &byteCounter);
			f_close(&fil);

		}

		vTaskDelay(500/portTICK_PERIOD_MS);

		// Writes a text file
		if(f_open(&fil, "textfile.txt", FA_OPEN_ALWAYS | FA_WRITE)==FR_OK){

			f_lseek(&fil, fil.fsize);
			sprintf(text, "Data1: %i\nData2: %i\nData3: %i\n", dataWrite.data1, dataWrite.data2, dataWrite.data3);
			f_puts(text, &fil);
			f_close(&fil);

		}

		vTaskDelay(500/portTICK_PERIOD_MS);

		// Reads a binary file
		if(f_open(&fil,"datafile.dat", FA_READ | FA_OPEN_EXISTING)==FR_OK){

			f_read(&fil, &dataRead, sizeof(MY_DATA), &byteCounter);
			f_close(&fil);

		}

		dataWrite.data1 = dataRead.data1 + 1;
		dataWrite.data2 = dataRead.data2 + 2;
		dataWrite.data3 = dataRead.data3 + 3;

		vTaskDelay(500/portTICK_PERIOD_MS);

	}
}

